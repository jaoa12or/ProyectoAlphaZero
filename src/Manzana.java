public class Manzana {

    private int posicionX;
    private int posicionY; 
    
    
    Manzana(int x, int y){
        posicionX = x;
        posicionY = y;
    }
    
    public int getPosicionX()
    {
        return posicionX;
    }
    
    public int getPosicionY()
    {
        return posicionY;
    }     

    
}
